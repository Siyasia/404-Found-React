const ADULT_THEME_OPTIONS = [
  'waterLily',
  'goldenHour',
  'blushCoast',
  'daisyMorning',
  'midnightVelvet',
  'royalSapphire',
  'emberwood',
  'verdantMoss',
]

export const ROLE_THEME_OPTIONS = {
  user: ADULT_THEME_OPTIONS,
  parent: ADULT_THEME_OPTIONS,
  child: ['playroom', 'rainbowCandy', 'spaceExplorer', 'jungleSafari'],
}

export const ROLE_DEFAULT_THEME = {
  user: 'waterLily',
  parent: 'waterLily',
  child: 'playroom',
}

export function getTheme(themeId) {
  return APP_THEMES[themeId] || APP_THEMES.waterLily
}

export function getAllowedThemesForRole(role) {
  return ROLE_THEME_OPTIONS[role] || ROLE_THEME_OPTIONS.user
}

export function getRoleDefaultTheme(role) {
  return ROLE_DEFAULT_THEME[role] || 'waterLily'
}

export function getThemesForRole(role) {
  const allowed = getAllowedThemesForRole(role)
  return allowed.map((themeId) => getTheme(themeId))
}
